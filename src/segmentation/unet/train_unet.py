def update_merged_df_paths(smart_base_path, smartom_base_path, df):
    """Prepend base paths to relative image/mask paths in merged dataset."""
    mask1 = df["source"] == "smart_II"
    mask2 = df["source"] == "smart_om"
    for col in ["image_path", "mask_path", "json_file"]:
        if col in df.columns:
            df.loc[mask1, col] = (
                smart_base_path + "/" + df.loc[mask1, col].astype(str)
            )
            df.loc[mask2, col] = (
                smartom_base_path + "/" + df.loc[mask2, col].astype(str)
            )
    return df


def update_paths(base_path, df):
    """Prepend base path to relative paths in augmented dataset."""
    for col in ["image_path", "mask_path", "json_file"]:
        if col in df.columns:
            df[col] = (
                base_path + "/" + df[col].astype(str).where(df[col].notna())
            )
    return df


def get_dataset_path(logger, config, cfg: UNetConfig) -> str:
    """
    Build the training CSV based on which dataset provides Normal images.

    Options (from unet.ini [DATASET] normal_dataset):
        SMART_II  : Normal from SMART_II only; OPMD+Variation from both
        SMART_OM  : Normal from SMART_OM only; OPMD+Variation from both
        BOTH      : All labels from both datasets
    """
    smart_merged_basepath = config.get("TRAIN", "smart.merged.basepath")
    smart_base            = config.get("TRAIN", "smart.basepath")
    smartom_base          = config.get("TRAIN", "smartom.basepath")
    augment_smart_base    = config.get("TRAIN", "augment.smart.baseroot")
    augment_smartom_base  = config.get("TRAIN", "augment.smartom.baseroot")

    merged_data_path = (
        f"{smart_merged_basepath}/"
        f"{config.get('SMART_MERGED', 'merged.coco.output.filename')}"
    )
    aug_smart_csv = (
        f"{augment_smart_base}/"
        f"{config.get('AUGMENT_SMART', 'augment.patient.coco.metadata.filename')}"
    )
    aug_smartom_csv = (
        f"{augment_smartom_base}/"
        f"{config.get('AUGMENT_SMARTOM', 'augment.patient.coco.metadata.filename')}"
    )

    dataset = pd.DataFrame()

    # Load merged dataset
    if Path(merged_data_path).exists():
        merged_df = pd.read_csv(merged_data_path)
        merged_df = update_merged_df_paths(smart_base, smartom_base, merged_df)
        logger.info("Loaded merged data: %s", merged_df.shape)
    else:
        raise FileNotFoundError(f"Merged dataset not found: {merged_data_path}")

    # Load augmented SMART-II
    if Path(aug_smart_csv).exists():
        aug_s = pd.read_csv(aug_smart_csv)
        aug_s = update_paths(augment_smart_base, aug_s)
        dataset = pd.concat([dataset, aug_s])
        logger.info("Added SMART-II augmented: %s", aug_s.shape)

    # Load augmented SMART-OM
    if Path(aug_smartom_csv).exists():
        aug_om = pd.read_csv(aug_smartom_csv)
        aug_om = update_paths(augment_smartom_base, aug_om)
        dataset = pd.concat([dataset, aug_om])
        logger.info("Added SMART-OM augmented: %s", aug_om.shape)

    # Filter by normal_dataset setting
    if cfg.normal_dataset == "SMART_II":
        temp = merged_df[
            (merged_df.source == "smart_II")
            | ((merged_df.source == "smart_om") & (merged_df.label != "normal"))
        ]
        dataset = pd.concat([dataset, temp])
    elif cfg.normal_dataset == "SMART_OM":
        temp = merged_df[
            (merged_df.source == "smart_om")
            | ((merged_df.source == "smart_II") & (merged_df.label != "normal"))
        ]
        dataset = pd.concat([dataset, temp])
    elif cfg.normal_dataset == "BOTH":
        dataset = pd.concat([dataset, merged_df])

    logger.info("Final dataset shape: %s", dataset.shape)

    # Save training CSV
    train_csv_path = config.get("SEGMENT-UNET", "train.dataset")
    Path(train_csv_path).parent.mkdir(parents=True, exist_ok=True)
    dataset = dataset.replace([np.nan, "nan", "NaN"], None)
    # Keep only rows with both image and mask
    df = dataset[dataset[["image_path", "mask_path"]].notnull().all(axis=1)].copy()
    logger.info("Rows with image_path and mask_path: %d", len(df))
    df.to_csv(train_csv_path, index=False)

    return train_csv_path


