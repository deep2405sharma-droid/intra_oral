"""
Interpretation of U-Net training results:

train_loss: Combined Dice + BCE loss over training set.
val_loss   : Same loss on validation set — watch for gap between train/val
             (large gap = overfitting).

mean_dice  : Mean Dice score across lesion classes (normal, opmd, variation),
             excluding background. PRIMARY metric for model selection.
             0.0 = no overlap, 1.0 = perfect overlap.

mean_iou   : Mean Intersection-over-Union across lesion classes.
             Related to Dice: IoU = Dice / (2 - Dice)
             Stricter than Dice.

pixel_acc  : Fraction of correctly classified pixels.
             WARNING: misleading metric — background pixels dominate.
             A model predicting background everywhere can get 90%+ accuracy.
             Always report Dice/IoU alongside pixel accuracy.

dice_opmd  : Dice score specifically for OPMD class.
             Most critical class clinically — low dice_opmd means the model
             is missing potentially malignant lesions.
             OPMD has fewest training samples → expect lowest Dice initially.

dice_variation: Dice for variation class.

dice_normal: Dice for normal tissue.

LR after step: Cosine-annealed learning rates for encoder and decoder.
               encoder LR starts lower (pretrained weights preserved).
               decoder LR starts higher (new weights need faster learning).

If dice_opmd = 0.0 after several epochs:
    → Model is ignoring OPMD class entirely due to class imbalance
    → Increase class_weights for OPMD (e.g. from 5.0 to 8.0)
    → Increase augmentation count for OPMD images
    → Check that mask PNGs have correct pixel values for OPMD (value=2)
"""