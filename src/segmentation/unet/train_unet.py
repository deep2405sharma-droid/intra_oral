"""
train_unet.py
-------------
Fine-tunes a U-Net (pretrained ResNet34 encoder) on the annotated
SMART intraoral dataset for semantic segmentation.

Differences from Mask R-CNN:
- U-Net does pixel-wise classification (semantic segmentation)
- No bounding boxes or instance separation
- Input: RGB image → Output: pixel mask (one class per pixel)
- Loss: Dice + BCE combined (handles class imbalance)
- Evaluation: Dice coefficient + IoU per class

Pipeline
--------
1. build_data_loaders()  →  patient-wise train / val split
2. build_unet_model()    →  ResNet34 encoder + U-Net decoder
3. Training loop         →  SGD with momentum + cosine LR decay
4. Validation loop       →  Dice and IoU per class
5. Checkpointing         →  best val Dice model saved; resume supported

Dataset contract
----------------
The CSV must have at minimum:
    image_path   : absolute path to the image
    mask_path    : absolute path to the binary mask PNG
                   (generated by prepare stage from VIA/COCO JSON)
    patient_id   : used for patient-wise train/val split
    label        : lesion label (normal / opmd / variation)

Mask PNG format:
    Single-channel PNG where pixel value = class id
    0 = background, 1 = normal, 2 = opmd, 3 = variation

Usage
-----
    python train_unet.py                        # uses config defaults
    python train_unet.py --profile kaggle       # uses kaggle_config.ini
"""

